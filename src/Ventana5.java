    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            int totalVagones = Integer.parseInt(txtTotalVagones.getText());
            int totalAsientos = Integer.parseInt(txtTotalAsientos.getText());
            int asientosEstandar = Integer.parseInt(txtAsientosEstandar.getText());
            int asientosEjecutivos = Integer.parseInt(txtAsientosEjecutivos.getText());
            int asientosPremium = Integer.parseInt(txtAsientosPremium.getText());
            int vagonesEquipaje = Integer.parseInt(txtVagonesEquipaje.getText());

            Vagon vagon = new Vagon(totalVagones, asientosEstandar, asientosEjecutivos, asientosPremium, vagonesEquipaje > 0);
            if (!sistema.getTrenes().isEmpty()) {
                Tren tren = sistema.getTrenes().get(0); // Ejemplo: primer tren
                tren.getVagones().add(vagon);
                JOptionPane.showMessageDialog(null, "Vagón agregado al tren");
            } else {
                JOptionPane.showMessageDialog(null, "No hay trenes disponibles. Agregue un tren primero.");
            }
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (vagonSeleccionado != -1 && !sistema.getTrenes().isEmpty()) {
            Tren tren = sistema.getTrenes().get(0); // Ejemplo: primer tren
            if (vagonSeleccionado < tren.getVagones().size()) {
                tren.getVagones().remove(vagonSeleccionado);
                JOptionPane.showMessageDialog(null, "Vagón eliminado");
                vagonSeleccionado = -1;
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "Vagón no encontrado");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un vagón para eliminar o asegúrese de que hay trenes");
        }
    }//GEN-LAST:event_jButton5ActionPerformed
private void limpiarCampos() {
        txtTotalVagones.setText("");
        txtTotalAsientos.setText("");
        txtAsientosEstandar.setText("");
        txtAsientosEjecutivos.setText("");
        txtAsientosPremium.setText("");
        txtVagonesEquipaje.setText("");
    }

