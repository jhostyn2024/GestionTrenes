    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String marca = txtMarca.getText();
            String ruta = txtRuta.getText();
            String horaSalida = txtHoraSalida.getText();
            String horaLlegada = txtHoraLlegada.getText();
            int capacidad = Integer.parseInt(txtCapacidad.getText());
            int vagones = Integer.parseInt(txtVagones.getText());

            Tren tren = new Tren("T" + (sistema.getTrenes().size() + 1), marca, marca, capacidad, 0.0, new ArrayList<>());
            if (trenSeleccionado == -1) {
                sistema.agregarTren(tren);
                JOptionPane.showMessageDialog(null, "Tren agregado");
            } else {
                sistema.modificarTren(trenSeleccionado, tren);
                JOptionPane.showMessageDialog(null, "Tren modificado");
            }
            trenSeleccionado = -1;
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (trenSeleccionado != -1) {
            try {
                sistema.eliminarTren(trenSeleccionado);
                JOptionPane.showMessageDialog(null, "Tren eliminado");
                trenSeleccionado = -1;
                limpiarCampos();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un tren para eliminar");
        }
    
    }//GEN-LAST:event_jButton4ActionPerformed

